// Lưu dữ liệu gốc
let originalData = JSON.parse(JSON.stringify(a));

// Biến phân trang
let currentPage = 1;
let itemsPerPage = 10;
let currentData = JSON.parse(JSON.stringify(a));

// Hàm hiển thị danh sách sản phẩm với phân trang
function displayProducts(products) {
    currentData = products;
    currentPage = 1;
    renderPage();
}

// Hàm render trang hiện tại

function renderPage() {
    const tbody = document.getElementById('productTable');
    tbody.innerHTML = '';

    if (currentData.length === 0) {
        tbody.innerHTML = '<tr><td colspan="6" class="text-center text-danger">Không tìm thấy sản phẩm nào</td></tr>';
        updatePaginationButtons();
        return;
    }

    // Tính toán start và end index
    const startIndex = (currentPage - 1) * itemsPerPage;
    const endIndex = startIndex + itemsPerPage;
    const pageData = currentData.slice(startIndex, endIndex);

    // Hiển thị dữ liệu trang hiện tại
    pageData.forEach(product => {
        const row = document.createElement('tr');
        row.innerHTML = `
            <td>${product.id}</td>
            <td>${product.title}</td>
            <td><span class="badge bg-info">${product.category.name}</span></td>
            <td><strong>${product.price.toLocaleString('vi-VN')} VNĐ</strong></td>
            <td>${product.description.substring(0, 50)}...</td>
            <td><img src="${product.images[0]}" alt="${product.title}" width="50" height="50" style="object-fit: cover; border-radius: 5px;"></td>
        `;
        tbody.appendChild(row);
    });

    updatePaginationButtons();
}

// Hàm cập nhật trạng thái nút phân trang
function updatePaginationButtons() {
    const totalPages = Math.ceil(currentData.length / itemsPerPage);
    
    // Cập nhật thông tin trang
    document.getElementById('pageInfo').textContent = `Trang ${currentPage} / ${totalPages}`;
    
    // Cập nhật nút Previous
    const prevBtn = document.getElementById('prevBtn');
    if (currentPage === 1) {
        prevBtn.classList.add('disabled');
    } else {
        prevBtn.classList.remove('disabled');
    }
    
    // Cập nhật nút Next
    const nextBtn = document.getElementById('nextBtn');
    if (currentPage === totalPages || totalPages === 0) {
        nextBtn.classList.add('disabled');
    } else {
        nextBtn.classList.remove('disabled');
    }
}

// Hàm chuyển trang trước
function previousPage() {
    if (currentPage > 1) {
        currentPage--;
        renderPage();
    }
}

// Hàm chuyển trang sau
function nextPage() {
    const totalPages = Math.ceil(currentData.length / itemsPerPage);
    if (currentPage < totalPages) {
        currentPage++;
        renderPage();
    }
}

// Hàm thay đổi số sản phẩm mỗi trang
function setItemsPerPage(num) {
    itemsPerPage = num;
    currentPage = 1;
    
    // Cập nhật trạng thái button
    document.querySelectorAll('[onclick*="setItemsPerPage"]').forEach(btn => {
        btn.classList.remove('active');
    });
    event.target.classList.add('active');
    
    renderPage();
}

// Hàm xử lý sắp xếp từ select
function handleSort() {
    const sortValue = document.getElementById('sortSelect').value;
    
    switch(sortValue) {
        case 'nameAsc':
            sortByNameAsc();
            break;
        case 'nameDesc':
            sortByNameDesc();
            break;
        case 'priceAsc':
            sortByPriceAsc();
            break;
        case 'priceDesc':
            sortByPriceDesc();
            break;
        case 'none':
            displayProducts(currentData);
            break;
    }
}

// Hàm xử lý thay đổi số sản phẩm trên trang từ select
function handleItemsPerPage() {
    const value = document.getElementById('itemsSelect').value;
    itemsPerPage = parseInt(value);
    currentPage = 1;
    renderPage();
}

// Hàm tìm kiếm theo tên
function handleSearch() {
    const searchInput = document.getElementById('searchInput').value.toLowerCase().trim();
    
    let filteredProducts;
    
    if (searchInput === '') {
        filteredProducts = JSON.parse(JSON.stringify(originalData));
    } else {
        filteredProducts = a.filter(product => 
            product.title.toLowerCase().includes(searchInput)
        );
    }
    
    displayProducts(filteredProducts);
}

